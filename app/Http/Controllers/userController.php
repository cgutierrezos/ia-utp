     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show(Request $request)
    {

        $user = User::where('name', $request->usuario)->where('password', $request->clave)->first();
        if(is_object($user)){
            $session =new session();
            $session->user_id=$user->id;

            return redirect('/');
        }else{
            return back()->withInput();
        }
        
    }

